package com.tooltwist.bsg.form;

public class HomeBuyerIndex {
	
	private String geographicalLevelName;
	private String homeBuyerIndex;
	private String postcode;
	private String state;
	private String suburb;
	public String getGeographicalLevelName()
	{
		return geographicalLevelName;
	}
	public void setGeographicalLevelName(String geographicalLevelName)
	{
		this.geographicalLevelName = geographicalLevelName;
	}
	public String getHomeBuyerIndex()
	{
		return homeBuyerIndex;
	}
	public void setHomeBuyerIndex(String homeBuyerIndex)
	{
		this.homeBuyerIndex = homeBuyerIndex;
	}
	public String getPostcode()
	{
		return postcode;
	}
	public void setPostcode(String postcode)
	{
		this.postcode = postcode;
	}
	public String getState()
	{
		return state;
	}
	public void setState(String state)
	{
		this.state = state;
	}
	public String getSuburb()
	{
		return suburb;
	}
	public void setSuburb(String suburb)
	{
		this.suburb = suburb;
	}
	
	

}
